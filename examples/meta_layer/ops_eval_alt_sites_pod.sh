#!/bin/bash
# Evaluate model on alternative splice sites.
# Supports both Eval-Ensembl-Alt and Eval-GENCODE-Alt protocols.
# (Formerly ops_eval_m2a_pod.sh / ops_eval_ensembl_alt_pod.sh)
#
# Tests whether the model can detect (alternative) splice sites present in a richer
# annotation (Ensembl or GENCODE) but absent from MANE.
#
# Reports two metrics sets:
#   1. Overall: all sites in the evaluation annotation
#   2. Alternative-only: sites in (annotation \ MANE)
#
# Prerequisites (on pod):
#   1. Package:          pip install -e ".[bio,conservation]"   (includes pyBigWig)
#   2. Model checkpoint: output/meta_layer/{m1s,m2c}/best.pt + config.pt
#   3. FASTA:            data/mane/GRCh38/Homo_sapiens.GRCh38.dna.primary_assembly.fa
#   4. Ensembl GTF:      data/ensembl/GRCh38/Homo_sapiens.GRCh38.112.gtf
#   5. Ensembl splices:  data/ensembl/GRCh38/splice_sites_enhanced.tsv
#   6. MANE splices:     data/mane/GRCh38/splice_sites_enhanced.tsv  (for set-diff)
#   7. Base scores:      /runpod-volume/data/ensembl/GRCh38/openspliceai_eval/precomputed/
#   8. BigWig cache:     /runpod-volume/bigwig_cache/*.bw
#   9. Junction data:    data/GRCh38/junction_data/junctions_gtex_v8.parquet
#  10. eCLIP data:       data/mane/GRCh38/rbp_data/eclip_peaks.parquet
#
#  For GENCODE additionally:
#  11. GENCODE GTF:      data/gencode/GRCh38/gencode.v47.annotation.gtf
#  12. GENCODE splices:  data/gencode/GRCh38/splice_sites_enhanced.tsv
#
# Data staging (run from LOCAL machine):
#   # Ensembl splice sites (generated by 04_generate_ground_truth.py)
#   rsync -Pavz data/ensembl/GRCh38/splice_sites_enhanced.tsv \
#       <cluster>:~/sky_workdir/data/ensembl/GRCh38/
#
#   # GENCODE data (if using gencode annotation)
#   rsync -Pavz data/gencode/GRCh38/splice_sites_enhanced.tsv \
#       <cluster>:~/sky_workdir/data/gencode/GRCh38/
#   rsync -Pavz data/gencode/GRCh38/gencode.v47.annotation.gtf \
#       <cluster>:~/sky_workdir/data/gencode/GRCh38/
#
#   # Model checkpoint (if not already on pod)
#   rsync -Pavz output/meta_layer/m1s/{best.pt,config.pt} \
#       <cluster>:~/sky_workdir/output/meta_layer/m1s/
#   # or for M2-S:
#   rsync -Pavz output/meta_layer/m2c/{best.pt,config.pt} \
#       <cluster>:~/sky_workdir/output/meta_layer/m2c/
#
#   # Updated source code
#   rsync -Pavz --include='*/' --include='*.py' --exclude='*' \
#       src/agentic_spliceai/ <cluster>:~/sky_workdir/src/agentic_spliceai/
#   rsync -Pavz --relative \
#       examples/meta_layer/09_evaluate_alternative_sites.py \
#       <cluster>:~/sky_workdir/
#
# Base scores (from precomputation, already on persistent volume):
#   /runpod-volume/data/ensembl/GRCh38/openspliceai_eval/precomputed/predictions_chr*.parquet
#
# Usage:
#   ssh <cluster>
#   cd ~/sky_workdir
#
#   # Eval-Ensembl-Alt with M1-S
#   bash examples/meta_layer/ops_eval_alt_sites_pod.sh m1s ensembl
#
#   # Eval-Ensembl-Alt with M2-S
#   bash examples/meta_layer/ops_eval_alt_sites_pod.sh m2s ensembl
#
#   # Eval-GENCODE-Alt with M2-S
#   bash examples/meta_layer/ops_eval_alt_sites_pod.sh m2s gencode

set -e

WORKDIR=~/sky_workdir

# ── Parse arguments ──────────────────────────────────────────────
MODEL=${1:-m1s}
ANNOTATION=${2:-ensembl}

# ── Resolve model checkpoint ─────────────────────────────────────
case "$MODEL" in
    m1s)  CHECKPOINT=$WORKDIR/output/meta_layer/m1s/best.pt ;;
    m2s)  CHECKPOINT=$WORKDIR/output/meta_layer/m2c/best.pt ;;
    *)    CHECKPOINT=$MODEL ;;
esac

# ── Resolve annotation source and paths ──────────────────────────
BIGWIG_CACHE=/runpod-volume/bigwig_cache
BASE_SCORES=/runpod-volume/data/ensembl/GRCh38/openspliceai_eval/precomputed
ANNOTATION_ARGS="--annotation-source $ANNOTATION"
BUILD_CACHE="--build-cache"

case "$ANNOTATION" in
    ensembl)
        OUTPUT_DIR=/runpod-volume/output/${MODEL}_eval_ensembl_alt
        CACHE_DIR=/runpod-volume/output/meta_layer/gene_cache_ensembl/test
        PROTOCOL="Eval-Ensembl-Alt"
        ;;
    gencode)
        OUTPUT_DIR=/runpod-volume/output/${MODEL}_eval_gencode_alt
        CACHE_DIR=/runpod-volume/output/meta_layer/gene_cache_gencode/test
        ANNOTATION_ARGS="$ANNOTATION_ARGS --gtf data/gencode/GRCh38/gencode.v47.annotation.gtf"
        PROTOCOL="Eval-GENCODE-Alt"
        ;;
    *)
        echo "ERROR: Unknown annotation: $ANNOTATION (use 'ensembl' or 'gencode')"
        exit 1
        ;;
esac

# Use pre-built cache if available (skip expensive cache building)
CACHE_ARGS=""
CACHE_COUNT=$(find "$CACHE_DIR" -name "*.npz" 2>/dev/null | wc -l)
if [ "$CACHE_COUNT" -gt 100 ]; then
    BUILD_CACHE=""
    CACHE_ARGS="--cache-dir $CACHE_DIR"
fi

cd "$WORKDIR"
mkdir -p "$OUTPUT_DIR"

echo "============================================================"
echo "$PROTOCOL ($MODEL) — $(date)"
echo "  Checkpoint:    $CHECKPOINT"
echo "  Annotation:    $ANNOTATION"
echo "  Base scores:   $BASE_SCORES"
echo "  BigWig:        $BIGWIG_CACHE"
echo "  Cache:         $CACHE_DIR ($CACHE_COUNT genes)"
echo "  Output:        $OUTPUT_DIR"
echo "============================================================"

# Verify prerequisites
echo "Checking prerequisites..."
ls "$CHECKPOINT" >/dev/null
ls "$(dirname "$CHECKPOINT")/config.pt" >/dev/null
ls data/mane/GRCh38/splice_sites_enhanced.tsv >/dev/null
ls "$BASE_SCORES"/predictions_chr1.parquet >/dev/null
ls data/GRCh38/junction_data/junctions_gtex_v8.parquet >/dev/null
ls data/mane/GRCh38/rbp_data/eclip_peaks.parquet >/dev/null
ls "$BIGWIG_CACHE"/hg38.phyloP100way.bw >/dev/null 2>&1 || echo "  Warning: BigWig cache not found"
if [ "$ANNOTATION" = "ensembl" ]; then
    ls data/ensembl/GRCh38/splice_sites_enhanced.tsv >/dev/null
elif [ "$ANNOTATION" = "gencode" ]; then
    ls data/gencode/GRCh38/gencode.v47.annotation.gtf >/dev/null
fi
echo "  All prerequisites present."

python -u examples/meta_layer/09_evaluate_alternative_sites.py \
    --checkpoint "$CHECKPOINT" \
    $ANNOTATION_ARGS \
    $BUILD_CACHE \
    $CACHE_ARGS \
    --base-scores-dir "$BASE_SCORES" \
    --bigwig-cache "$BIGWIG_CACHE" \
    --output-dir "$OUTPUT_DIR" \
    --device cuda

echo "============================================================"
echo "$PROTOCOL ($MODEL) complete — $(date)"
echo "Results: $OUTPUT_DIR/"
echo "============================================================"