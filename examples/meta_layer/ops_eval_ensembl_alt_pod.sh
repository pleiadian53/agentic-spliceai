#!/bin/bash
# Eval-Ensembl-Alt: Evaluate model on alternative splice sites (Ensembl \ MANE).
# (Formerly ops_eval_m2a_pod.sh)
#
# Tests whether multimodal features help the model generalize to splice
# sites it was never trained on (present in Ensembl but not MANE).
#
# Reports two metrics sets:
#   1. Overall: all Ensembl sites on test chromosomes
#   2. Alternative-only: sites in (Ensembl \ MANE)
#
# Prerequisites (on pod):
#   1. Package:          pip install -e ".[bio,conservation]"   (includes pyBigWig)
#   2. M1-S model:       output/meta_layer/m1s/best.pt + config.pt
#   3. FASTA:            data/mane/GRCh38/Homo_sapiens.GRCh38.dna.primary_assembly.fa
#   4. Ensembl GTF:      data/ensembl/GRCh38/Homo_sapiens.GRCh38.112.gtf
#   5. Ensembl splices:  data/ensembl/GRCh38/splice_sites_enhanced.tsv
#   6. MANE splices:     data/mane/GRCh38/splice_sites_enhanced.tsv  (for set-diff)
#   7. Ensembl base scores: /runpod-volume/data/ensembl/GRCh38/openspliceai_eval/precomputed/
#   8. BigWig cache:     /runpod-volume/bigwig_cache/*.bw
#   9. Junction data:    data/mane/GRCh38/junction_data/junctions_gtex_v8.parquet
#  10. eCLIP data:       data/mane/GRCh38/rbp_data/eclip_peaks.parquet
#
# Data staging (run from LOCAL machine):
#   # Ensembl splice sites (generated by 04_generate_ground_truth.py)
#   rsync -Pavz data/ensembl/GRCh38/splice_sites_enhanced.tsv \
#       <cluster>:~/sky_workdir/data/ensembl/GRCh38/
#
#   # M1-S checkpoint (if not already on pod)
#   rsync -Pavz output/meta_layer/m1s/{best.pt,config.pt} \
#       <cluster>:~/sky_workdir/output/meta_layer/m1s/
#
#   # Updated source code
#   rsync -Pavz --include='*/' --include='*.py' --exclude='*' \
#       src/agentic_spliceai/ <cluster>:~/sky_workdir/src/agentic_spliceai/
#   rsync -Pavz --relative \
#       examples/meta_layer/09_evaluate_alternative_sites.py \
#       <cluster>:~/sky_workdir/
#
# Ensembl base scores (from precomputation, already on persistent volume):
#   /runpod-volume/data/ensembl/GRCh38/openspliceai_eval/precomputed/predictions_chr*.parquet
#
# Usage:
#   ssh <cluster>
#   cd ~/sky_workdir
#   nohup bash examples/meta_layer/ops_eval_m2a_pod.sh \
#       > /runpod-volume/output/m2a_eval/eval.log 2>&1 &

set -e

WORKDIR=~/sky_workdir
CHECKPOINT=$WORKDIR/output/meta_layer/m1s/best.pt
BIGWIG_CACHE=/runpod-volume/bigwig_cache
ENSEMBL_SCORES=/runpod-volume/data/ensembl/GRCh38/openspliceai_eval/precomputed
OUTPUT_DIR=/runpod-volume/output/m2a_eval

cd "$WORKDIR"
mkdir -p "$OUTPUT_DIR"

echo "============================================================"
echo "M2a Evaluation (Ensembl \\ MANE) — $(date)"
echo "  Checkpoint:    $CHECKPOINT"
echo "  Base scores:   $ENSEMBL_SCORES"
echo "  BigWig:        $BIGWIG_CACHE"
echo "  Output:        $OUTPUT_DIR"
echo "============================================================"

# Verify data is staged
echo "Checking prerequisites..."
ls "$CHECKPOINT" >/dev/null
ls "$WORKDIR/output/meta_layer/m1s/config.pt" >/dev/null
ls data/ensembl/GRCh38/splice_sites_enhanced.tsv >/dev/null
ls data/mane/GRCh38/splice_sites_enhanced.tsv >/dev/null
ls "$ENSEMBL_SCORES"/predictions_chr1.parquet >/dev/null
ls data/mane/GRCh38/junction_data/junctions_gtex_v8.parquet >/dev/null
ls data/mane/GRCh38/rbp_data/eclip_peaks.parquet >/dev/null
ls "$BIGWIG_CACHE"/hg38.phyloP100way.bw >/dev/null
python -c "import pyBigWig" 2>/dev/null || { echo "ERROR: pyBigWig not installed"; exit 1; }
echo "  All prerequisites present."

python -u examples/meta_layer/09_evaluate_alternative_sites.py \
    --checkpoint "$CHECKPOINT" \
    --annotation-source ensembl \
    --build-cache \
    --base-scores-dir "$ENSEMBL_SCORES" \
    --bigwig-cache "$BIGWIG_CACHE" \
    --output-dir "$OUTPUT_DIR" \
    --device cuda

echo "============================================================"
echo "M2a evaluation complete — $(date)"
echo "Results: $OUTPUT_DIR/m2a_eval_results.json"
echo "============================================================"
